public class IsNumberZero {

	public static void main(String[] args) {
		int number = 0;
		if(number != 0) {
			if(number > 0) {
				System.out.println("Number is higher.");
			}else {
				System.out.println("Number is lower.");
			}
		}else {
			System.out.println("It is zero.");
		}
		

	}

}
